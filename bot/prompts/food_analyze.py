    return f"""
    Проанализируй еду на изображении по ссылке: {image_url}

    Ответ строго в JSON-формате. Включи:
    - название блюда
    - общий вес
    - калории, белки, жиры, углеводы, клетчатка
    - список ингредиентов с аналогичными данными
    """
